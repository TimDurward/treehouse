/*******************************************
*********LOOP DEMONSTRATIONS****************
*******************************************/

//FOR LOOPS
for(var i=1; i<=100; i+=1){
	document.write("<h3>"+i+"</h3><br>");
}


// Do...While LOOPS
var i = 1;
do{
document.write("<h3>"+i+"</h3><br>");
i += 1;
} while(i<=100)

	
// While LOOPS	
var i = 1;
while(i<=100){
	document.write("<h3>"+i+"</h3><br>");
	i += 1;
}